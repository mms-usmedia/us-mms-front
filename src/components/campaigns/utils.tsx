import React from "react";

// Utilidades compartidas para componentes de campaña

/**
 * Formatea un valor de moneda con el símbolo $ y separadores de miles
 */
export const formatCurrency = (amount: number) => {
  return (
    "$" +
    amount.toLocaleString("en-US", {
      minimumFractionDigits: 2,
      maximumFractionDigits: 2,
    })
  );
};

/**
 * Formatea un número con separadores de miles
 */
export const formatNumber = (num: number, decimals: number = 0) => {
  return new Intl.NumberFormat("en-US", {
    minimumFractionDigits: decimals,
    maximumFractionDigits: decimals,
  }).format(num);
};

/**
 * Formatea una fecha en formato MM/DD/YYYY
 */
export const formatDate = (dateStr: string) => {
  const date = new Date(dateStr);
  return date.toLocaleDateString("en-US", {
    year: "numeric",
    month: "2-digit",
    day: "2-digit",
  });
};

/**
 * Trunca un texto si es más largo que maxLength y añade puntos suspensivos
 */
export const truncateText = (text: string, maxLength: number) => {
  return text.length > maxLength ? text.substring(0, maxLength) + "..." : text;
};

/**
 * Formatea el tamaño de un archivo a KB, MB, etc.
 */
export const formatFileSize = (bytes: number) => {
  if (bytes === 0) return "0 Bytes";
  const k = 1024;
  const sizes = ["Bytes", "KB", "MB", "GB"];
  const i = Math.floor(Math.log(bytes) / Math.log(k));
  return parseFloat((bytes / Math.pow(k, i)).toFixed(2)) + " " + sizes[i];
};

